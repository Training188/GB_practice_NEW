'''Для списка реализовать обмен значений соседних элементов, т.е.
Значениями обмениваются элементы с индексами 0 и 1, 2 и 3 и т.д.
При нечетном количестве элементов последний сохранить
на своем месте.
Для заполнения списка элементов необходимо использовать
функцию input().'''

my_list = list(map(int, input('Enter list: ').split(',')))
x = 0
for el in range(int(len(my_list)/2)):
    my_list[x], my_list[x + 1] = my_list[x + 1], my_list[x]
    x += 2
print(my_list)